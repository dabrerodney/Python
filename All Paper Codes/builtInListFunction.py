#to find the length of the string
list1 = [10, 20, 30, 40]
a = len(list1)
print(a)

#to find the sum of all elements in the list
b = sum(list1)
print(b)

c = max(list1)
print(c)

d = min(list1)
print(d)

#to reverse the elements in the list
list1.reverse()
print(list1)

#to sort all the elements in the list
list2 = [9, 0, 2, 99, 1]
list2.sort()
print(list2)

#to remove the specified element from the list
list2.remove(0)
print(list2)